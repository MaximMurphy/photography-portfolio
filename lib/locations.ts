export async function getLocationData(
  slug: string
): Promise<Location | undefined> {
  const location = locations.find((location) => location.slug === slug);

  if (location) {
    try {
      // Attempt to preload images
      await Promise.all(
        location.photos.map(async (photo) => {
          try {
            const response = await fetch(photo.src, {
              method: "HEAD",
              cache: "force-cache",
            });

            if (response.ok) {
              photo.preloaded = true;
            }
          } catch (error) {
            console.error(`Failed to precheck image: ${photo.src}`, error);
          }
        })
      );
    } catch (error) {
      console.error("Error preloading images", error);
    }
  }

  return location;
}

export function getAllLocations(): Location[] {
  return locations;
}
